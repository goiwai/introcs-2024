// include for std::cout
#include <iostream>

// the main function the start point of executable file in C/C++
// main returns integer value
// main takes the void argument, which means takes no argument here.
int main() {
  // std::cout comes first, then followed by "<<", followed by what to be printed out
  std::cout << "Hello, World!" << std::endl;

  // 0 means the program finished successfully in the Linux/UNIX systems.
  return 0;
}
